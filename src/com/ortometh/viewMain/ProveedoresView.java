    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Eliminar Proveedor
        int confirm = JOptionPane.showConfirmDialog(null, "¿Confirma la eliminación?", "Eliminar proveedor", JOptionPane.YES_NO_OPTION);
        System.out.println("Opcion: " + confirm);
        if (confirm == 0) {
            int proveedorID = Integer.parseInt(txtproveedorID.getText());
            String message = proveedorController.eliminarProveedor(proveedorID);
            recargarTabla();
            JOptionPane.showMessageDialog(null, message, "Eliminar Proveedor", JOptionPane.INFORMATION_MESSAGE);
            clearTextFileds();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

