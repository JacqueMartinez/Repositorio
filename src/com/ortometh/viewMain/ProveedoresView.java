    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (!txtFiltrar.getText().equals("")) {
            String parametro = txtFiltrar.getText();
            if (cbxBusqueda.getSelectedIndex() == 0) {
                if (!proveedorController.getProveedorByNombre(tableProveedores, parametro)) {
                    recargarTabla();
                    JOptionPane.showMessageDialog(null, "Verifique los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
                txtFiltrar.setText(null);
            } else if (cbxBusqueda.getSelectedIndex() == 1) {
                if (!proveedorController.getProveedorByRFC(tableProveedores, parametro)) {
                    recargarTabla();
                    JOptionPane.showMessageDialog(null, "Verifique los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
                txtFiltrar.setText(null);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo vacío", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

