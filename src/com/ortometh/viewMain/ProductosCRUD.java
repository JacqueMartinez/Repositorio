    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            if (txtId_producto.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Seleccione un producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                int confirm = JOptionPane.showConfirmDialog(null, "¿Confirma la eliminación?", "Eliminar producto", JOptionPane.YES_NO_OPTION);
                System.out.println("Opcion: " + confirm);
                if (confirm == 0) {
                    String clave_producto = txtId_producto.getText();
                    if (productoController.eliminarProducto(clave_producto) == true) {
                        JOptionPane.showMessageDialog(null, "Producto eliminado", null, JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "No se a podido eliminar", null, JOptionPane.INFORMATION_MESSAGE);
                    }
                    try {
                        productoController.refillProducto(jTable1);
                    } catch (SQLException ex) {
                        Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila < 0) {
             JOptionPane.showMessageDialog(null, "Seleccione un producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {

            if (txtId_producto.getText().equals("")) {
                 JOptionPane.showMessageDialog(null, "Seleccione un producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                ProductosCRUDIModificar productosCRUDIModificar = new ProductosCRUDIModificar(usuario);
                productosCRUDIModificar.setVisible(true);
                this.setVisible(false);
                //Obtener valores de la tabla
                String tipo_producto = jTable1.getValueAt(fila, 2).toString();
                String clave = jTable1.getValueAt(fila, 4).toString();
                String marca = jTable1.getValueAt(fila, 5).toString();
                String categoria = jTable1.getValueAt(fila, 1).toString();
                String proveedor = jTable1.getValueAt(fila, 3).toString();
                String descripcion = jTable1.getValueAt(fila, 6).toString();
                String stock = jTable1.getValueAt(fila, 7).toString();
                String stock_min = jTable1.getValueAt(fila, 8).toString();
                String precio_venta = jTable1.getValueAt(fila, 9).toString();
                String precio_compra = jTable1.getValueAt(fila, 10).toString();
                //Mandar valores a la vista de modificar
                ProductosCRUDIModificar.tipo_producto.setSelectedItem(tipo_producto);
                ProductosCRUDIModificar.txtclave.setText(String.valueOf(clave));
                ProductosCRUDIModificar.txtmarca.setText(marca);
                ProductosCRUDIModificar.categoria.setSelectedItem(categoria);
                ProductosCRUDIModificar.proveedor.setSelectedItem(proveedor);
                ProductosCRUDIModificar.txtdescripcion.setText(descripcion);
                ProductosCRUDIModificar.txtstock.setText(String.valueOf(stock));
                ProductosCRUDIModificar.txtstock_min.setText(String.valueOf(stock_min));
                ProductosCRUDIModificar.txtPrecio_venta.setText(String.valueOf(precio_venta));
                ProductosCRUDIModificar.txtPrecio_compra.setText(String.valueOf(precio_compra));
                ProductosCRUDIModificar.txtid_producto.setText(txtId_producto.getText());
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

