    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (ComboFiltro.getSelectedIndex() == 0 || comboFiltro2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un filtro de busqueda", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            if (ComboFiltro.getSelectedIndex() == 3) {
                String prov = comboFiltro2.getSelectedItem().toString();
                int id_proveedor = 0;
                try {
                    id_proveedor = productoController.id_proveedor(prov);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    productoController.filtrarProductoProveedor(jTable1, id_proveedor);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            //categoria
            if (ComboFiltro.getSelectedIndex() == 1) {
                String categoria = comboFiltro2.getSelectedItem().toString();
                int id_categoria = 0;
                try {
                    id_categoria = productoController.id_categoria(categoria);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    productoController.filtrarProductoCategoria(jTable1, id_categoria);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            //tipo
            if (ComboFiltro.getSelectedIndex() == 2) {
                String tipo = comboFiltro2.getSelectedItem().toString();
                int id_tipo = 0;
                try {
                    id_tipo = productoController.Id_tipo(tipo);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    productoController.filtrarProductoTipo(jTable1, id_tipo);
                } catch (SQLException ex) {
                    Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRecargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecargarActionPerformed
        txtId_producto.setText("");
        try {
            productoController.refillProducto(jTable1);
            if (comboFiltro2.getSelectedIndex() != 0 && ComboFiltro.getSelectedIndex() != 0) {
                comboFiltro2.setSelectedIndex(0);
                ComboFiltro.setSelectedIndex(0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductosCRUD.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRecargarActionPerformed

    private void ComboFiltroItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboFiltroItemStateChanged
        //categoria
        if (ComboFiltro.getSelectedIndex() == 0) {
            comboFiltro2.removeAllItems();
        }
        //categoria
        if (ComboFiltro.getSelectedIndex() == 1) {
            productoController.fillComboCategoria(comboFiltro2);
        }
        //tipo
        if (ComboFiltro.getSelectedIndex() == 2) {
            productoController.fillCombotTipo_producto(comboFiltro2);
        }
        //proveedor
        if (ComboFiltro.getSelectedIndex() == 3) {
            productoController.fillComboProveedor(comboFiltro2);
        }
    }//GEN-LAST:event_ComboFiltroItemStateChanged

