    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
 
        Date fecha1 = (Date) jDateChooser1.getDate();
        Date fecha2 = (Date) jDateChooser2.getDate();

        DateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        String fecha_1 = f.format(fecha1);
        String fecha_2 = f.format(fecha2);
        
        if (fecha_1 != "") {
            if (fecha_2 != "") {
                controller.corteCaja(fecha_1 + " 00:00:00", fecha_2 + " 23:59:59");
                controller.corteCajaTabla(jTable1);
            } else {
                JOptionPane.showMessageDialog(null, "Debe llenar la fecha de termino");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe llanar la fecha de inicio");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteActionPerformed
        Date fecha_1 = (Date) jDateChooser1.getDate();
        Date fecha_2 = (Date) jDateChooser2.getDate();

        DateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        String fecha1 = f.format(fecha_1);
        String fecha2 = f.format(fecha_2);
        if (fecha1 != "") {
            if (fecha2 != "") {
                controller.corteCaja(fecha1 + " 00:00:00", fecha2 + " 23:59:59");
                controller.reporteCorteCaja(usuario.getNombreUsuario());
            } else {
                JOptionPane.showMessageDialog(null, "Debe llenar la fecha de termino");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe llanar la fecha de inicio");
        }
    }//GEN-LAST:event_btnReporteActionPerformed

