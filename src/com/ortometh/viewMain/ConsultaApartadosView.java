    private void cbFiltroApartadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFiltroApartadoItemStateChanged
        String item = cbFiltroApartado.getSelectedItem().toString();
        if (item.equalsIgnoreCase("Clave")) {
            lblDato.setText("Ingrese la clave del apartado");
        } else if (item.equalsIgnoreCase("Cliente")) {
            lblDato.setText("Ingrese el nombre del cliente");
        } else if (item.equalsIgnoreCase("Fecha")) {
            lblDato.setText("Ingese la fecha AAAA-MM-DD");
        } else if (item.equalsIgnoreCase("Estado")) {
            lblDato.setText("Ingrese el estado (Proceso/Finalizado)");
        }
        txtFiltroApartado.requestFocus();
    }//GEN-LAST:event_cbFiltroApartadoItemStateChanged

