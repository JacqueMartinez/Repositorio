    private void datos() {
        int rowCount = jTable1.getRowCount();
        int row = jTable1.getSelectedRow();
        int rowc = jTable1.getSelectedRowCount();
        if (rowCount >= 1) {
            if (rowc == 1) {
                apartado = String.valueOf(jTable1.getValueAt(row, 0));
                cliente = String.valueOf(jTable1.getValueAt(row, 1));
                costo = String.valueOf(jTable1.getValueAt(row, 2));
                resto = String.valueOf(jTable1.getValueAt(row, 3));
                fecha = String.valueOf(jTable1.getValueAt(row, 4));
                observaciones = String.valueOf(jTable1.getValueAt(row, 5));
            } else {
                JOptionPane.showMessageDialog(null, "Debe selecionar un solo registro");
            }
        }
    }

