    private void txtFiltroApartadoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroApartadoKeyReleased
        if (rbProceso.isSelected()) {
            if (txtFiltroApartado.getText() != null) {
                apartadosController.consultarApartadosProceso(jTable1, txtFiltroApartado.getText(), String.valueOf(cbFiltroApartado.getSelectedItem()));
            } else {
                apartadosController.consultarApartadosProceso(jTable1);
            }
        } else if (rbFinalizado.isSelected()) {
            if (txtFiltroApartado.getText() != null) {
                apartadosController.consultarApartadosFinalizados(jTable1, txtFiltroApartado.getText(), String.valueOf(cbFiltroApartado.getSelectedItem()));
            } else {
                apartadosController.consultarApartadosFinalizados(jTable1);
            }
        }
    }//GEN-LAST:event_txtFiltroApartadoKeyReleased

