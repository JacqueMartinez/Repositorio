    private void jTableInfoVentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableInfoVentaMouseClicked
        // TODO add your handling code here:
        int fila = jTableInfoVenta.getSelectedRow();

        int id_ventaProducto = (int) jTableInfoVenta.getValueAt(fila, 0);
        int id_Producto = (int) jTableInfoVenta.getValueAt(fila, 1);
        String producto = jTableInfoVenta.getValueAt(fila, 2).toString();
        int cantidad = (int) jTableInfoVenta.getValueAt(fila, 3);
        int cantidadDevueltos = (int) jTableInfoVenta.getValueAt(fila, 6);

        int cantidadLimite = cantidad - cantidadDevueltos;

        txtIDProducto.setText(String.valueOf(id_Producto));
        txtProducto.setText(producto);
        txtCantidad.setText(String.valueOf(cantidadLimite));
        txtCantidadLimite.setText(String.valueOf(cantidadLimite));
        txtIdVentaProducto.setText(String.valueOf(id_ventaProducto));
    }//GEN-LAST:event_jTableInfoVentaMouseClicked

