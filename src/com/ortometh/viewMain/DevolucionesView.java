    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        //Devolver Producto
        if (txtProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Seleccione un Producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            if (txtCantidad.getText().equals("") || txtNombreCliente.getText().equals("") || txaComentario.getText().equals("") || jDateFechaEntrega.equals("")) {
                JOptionPane.showMessageDialog(null, "Llene todos los campos", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {

                int idVenta = Integer.parseInt(txtIDVenta.getText());
                int idProducto = Integer.parseInt(txtIDProducto.getText());
                int idUsuario = Integer.parseInt(txtIdUsuarioLogin.getText());
                int cantidad = Integer.parseInt(txtCantidad.getText());
                int cantidadLimite = Integer.parseInt(txtCantidadLimite.getText());
                int idVentaProducto = Integer.parseInt(txtIdVentaProducto.getText());
                String tipo = (String) cbxTipoDevolucion.getSelectedItem();
                System.out.println(tipo);
                String comentario = txaComentario.getText();
                String nombreCliente = txtNombreCliente.getText();

                if (cantidad <= cantidadLimite) {
                    if (tipo.equals("Inventario")) {

                        String message = devController.devolverProductoInventario(idVentaProducto, idVenta, idProducto, cantidad, tipo, comentario, idUsuario, nombreCliente);
                        JOptionPane.showMessageDialog(null, message);
                        clearTextFileds();
                    } else if (tipo.equals("Proveedor")) {
                        Date date = (Date) jDateFechaEntrega.getDate();
                        DateFormat fecha = new SimpleDateFormat("yyyy-MM-dd");
                        String fechaEntrega = fecha.format(date);

                        String message = devController.devolverProductoProveedor(idVentaProducto, idVenta, idProducto, cantidad, tipo, comentario, idUsuario, nombreCliente, fechaEntrega);
                        JOptionPane.showMessageDialog(null, message);
                        clearTextFileds();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Producto insuficiente", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnDevolverActionPerformed

