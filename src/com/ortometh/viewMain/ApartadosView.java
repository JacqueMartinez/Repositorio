    private void btnAbonarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbonarActionPerformed
        if (lblResto.getText().equals("0.0")) {
            JOptionPane.showMessageDialog(null, "El apartado esta totalmente pagado");
        } else {
            if (d == 1) {
                if (txtCliente.getText().isEmpty() || txtObservaciones.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Debe llenar los campos obligatorios", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                } else {
                    agregar();
                    d = 0;
                    idApartado = Integer.parseInt(apartadosController.idApartado(claveA));
                    lblResto.setText(apartadosController.restante(idApartado));
                    bloquear();
                    AbonosView atv = new AbonosView(idApartado, resto, costo, usuario);
                    atv.setVisible(true);
                    atv.setLocationRelativeTo(null);
                    this.dispose();
                }
            } else {
                AbonosView atv = new AbonosView(idApartado, resto, costo, usuario);
                atv.setVisible(true);
                atv.setLocationRelativeTo(null);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnAbonarActionPerformed

    private void datos() {
        ArrayList<Apartados> a = dao.consultarApartados(idApartado);
        int rows = a.size();
        for (int i = 0; i < rows; i++) {
            claveA = a.get(i).getClaveApartado();
            cliente = a.get(i).getCliente();
            proveedor = a.get(i).getProveedor();
            doctor = a.get(i).getDoctor();
            clinica = a.get(i).getClinica();
            numeroCuarto = a.get(i).getNumeroCuarto();
            observaciones = a.get(i).getObservaciones();
            fecha = a.get(i).getFecha();
            costo = a.get(i).getCosto();
            resto = a.get(i).getRestantante();
        }

    }

    private void rellenar() {
        lblApartado.setText(claveA);
//        cbProveedor.setSelectedIndex(proveedor);
        txtCliente.setText(cliente);
        txtDoctor.setText(doctor);
        txtClinica.setText(clinica);
        txtCuarto.setText(numeroCuarto);
        lblFecha.setText(fecha);
        lblResto.setText(String.valueOf(resto));
        lblCosto.setText(String.valueOf(costo));
        txtObservaciones.setText(observaciones);
    }

    private void agregar() {
//        proveedor = Integer.parseInt(String.valueOf(cbProveedor.getSelectedIndex()));
        doctor = txtDoctor.getText();
        clinica = txtClinica.getText();
        numeroCuarto = String.valueOf(txtCuarto.getText());
        cliente = txtCliente.getText();
        observaciones = txtObservaciones.getText();
        apartadosController.llenarDatos(idApartado, proveedor, cliente, doctor, clinica, numeroCuarto, observaciones);
    }

    private void bloquear() {
//        cbProveedor.setEnabled(false);
        txtClinica.setEnabled(false);
        txtCuarto.setEnabled(false);
        txtDoctor.setEnabled(false);
        txtCliente.setEnabled(false);
        txtObservaciones.setEnabled(false);
    }

