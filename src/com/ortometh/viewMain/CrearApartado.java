    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String codigo_producto = txtProducto.getText();
        if (codigo_producto.isEmpty() || txtCantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese la clave del producto y la cantidad nueva");
        } else {
            int cantidad = Integer.parseInt(txtCantidad.getText());
            int idProducto = Integer.parseInt(controller.idProducto(codigo_producto));
            controller.cambiarCantidad(idApartado, idProducto, cantidad);
            controller.listProductos(jtProductos, idApartado);
            lblPiezas.setText(controller.piezas(idApartado));
            lblSubtotal.setText(String.valueOf(controller.subtotal(idApartado)));
            lblTotal.setText(String.valueOf(controller.importe(idApartado)));
            limpiar();
            txtProducto.requestFocus();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

