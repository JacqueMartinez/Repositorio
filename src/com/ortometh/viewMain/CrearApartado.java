    private void txtProductoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProductoKeyReleased
        if (txtProducto.getText().length() == 8) {
            double descuento = 0.0d;
            String producto = controller.idProducto(txtProducto.getText());
            int idProducto = 0;
            if (producto != "") {
                idProducto = Integer.parseInt(producto);
            }
            if (txtProducto.getText().isEmpty() || txtCantidad.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe llenar los campos obligatorios", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            } else {
                if (idProducto < 1) {
                    JOptionPane.showMessageDialog(null, "El producto no existe", "ERROR", JOptionPane.ERROR_MESSAGE);
                    limpiar();
                    txtProducto.requestFocus();
                } else {
                    int cantidad = Integer.parseInt(txtCantidad.getText());
                    if (!txtDescuento.getText().isEmpty()) {
                        descuento = Double.parseDouble(txtDescuento.getText());
                    }
                    controller.llenarApartado(idApartado, idProducto, descuento, cantidad, usuario.getIdUserLog());
                    lblPiezas.setText(controller.piezas(idApartado));
                    lblSubtotal.setText(String.valueOf(controller.subtotal(idApartado)));
                    lblTotal.setText(String.valueOf(controller.importe(idApartado)));
                    controller.listProductos(jtProductos, idApartado);
                    limpiar();
                    txtProducto.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_txtProductoKeyReleased


