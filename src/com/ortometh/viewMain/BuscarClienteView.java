    private void jtableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtableKeyPressed
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
        int fila = jtable.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        } else {
            String nombre = jtable.getValueAt(fila, 1).toString().toUpperCase();
            String apellido_paterno = jtable.getValueAt(fila, 2).toString().toUpperCase();
            String apellido_materno = jtable.getValueAt(fila, 3).toString().toUpperCase();
            Venta.txtNombreCliente.setText(nombre+ " " + apellido_paterno + " " + apellido_materno);
            this.setVisible(false);
        }
        }
    }//GEN-LAST:event_jtableKeyPressed

