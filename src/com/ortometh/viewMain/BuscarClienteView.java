    private void rSButtonMetro4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonMetro4ActionPerformed
        if (txtfiltro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del cliente");
        } else {
            String nombre = txtfiltro.getText();
            try {
                productoController.fillClienteNombre(jtable, nombre);
            } catch (SQLException ex) {
                Logger.getLogger(BuscarClienteView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        txtfiltro.setText("");
    }//GEN-LAST:event_rSButtonMetro4ActionPerformed

